# Generates sequence of numbers to count up from 0-50 
for i in range(51): 
    print(i, end= " ")

# Generates sequence of numbers to count down from 50-0
for i in range(50, -1, -1): 
    print(i, end= " ")

# Generates sequence of numbers to count between 30-50 
for i in range(30, 51): 
    print(i, end= " ")

# Generates sequence of numbers to count down from 50-10 with two decrements
for i in range(50, 9, -2):
    print(i, end= " ")

# Generates sequence of numbers to count up from 100-200 with five decrements
for i in range(100, 201, 5): 
    print(i, end= " ")